	void loadTable() throws UnknownHostException, IOException,
			ClassNotFoundException {
		Company c = new Company();
		companys = c.viewData();
		Object title[] = { "公司编号", "公司名", "注册人" };
		Object[][] detail = new Object[companys.size()][3];
		for (int i = 0; i < companys.size(); i++) {
			detail[i][0] = companys.get(i).getC_id();
			detail[i][1] = companys.get(i).getC_name();
			detail[i][2] = companys.get(i).getC_regist();

		}
		this.jTable1.setModel(new DefaultTableModel(detail, title));
	}
	
	
