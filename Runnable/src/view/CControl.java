	private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
		// TODO add your handling code here:
		int row = jTable1.getSelectedRow();
		int column = jTable1.getSelectedColumn();
		if (row == -1 || column == 0)
			return;

		String change = (String) jTable1.getValueAt(row, column);
		String c_id = companys.get(row).getC_id();
		String select = TITLES2.get(column);
		try {
			Client c = new Client();
			Company cc = c.UpdateComp(select, c_id, change);
		} catch (UnknownHostException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		} catch (ClassNotFoundException e) {
			e.printStackTrace();
		}

		JOptionPane.showMessageDialog(null, "更改成功", "提示",
				JOptionPane.PLAIN_MESSAGE);
	}

	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
		// TODO add your handling code here:
		int row = jTable1.getSelectedRow();
		String c_id = companys.get(row).getC_id();

		try {
			Client c = new Client();
			Company cc = c.DeleteComp(c_id);
		} catch (IOException e) {
			e.printStackTrace();
		} catch (ClassNotFoundException e) {
			e.printStackTrace();
		}

		JOptionPane.showMessageDialog(null, "删除成功", "提示",
				JOptionPane.PLAIN_MESSAGE);
		companys.remove(row);
		// 更新表格
		jTable1.validate();
		jTable1.updateUI();
	}

